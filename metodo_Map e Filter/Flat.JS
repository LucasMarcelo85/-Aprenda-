

// O método .flat() em JavaScript é usado para “achatar” um array, ou seja, 
// transformar um array aninhado (com sub-arrays) em um único array.
const myArr = [[1, 2], [3, 4], [5, 6]];
console.log (myArr)

// Se você quiser criar um novo array com todos os elementos concatenados, use o .flat() assim:

const newArr = myArr.flat();
console.log(newArr)
